function NoneSemanticHeading() {
    return (
        <div class="header">
            <div class="logo">Company Logo</div>
            <div class="title">Page Title</div>
        </div>
    )
}

function SemanticHeading() {
    return (
        <header>
            <img src="logo.png" alt="Company Logo" />
            <h1>Page Title</h1>
        </header>
    )
}